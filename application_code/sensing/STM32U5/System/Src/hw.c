void hw_tim5_init( void )
{
  static TIM_HandleTypeDef xTim5Handle =
  {
      .Instance       = TIM5,
      .Init.Prescaler = TIMER_PRESCALER,
      .Init.Period    = 0xFFFFFFFF,
  };

  __TIM5_CLK_ENABLE();

  if( HAL_OK == HAL_TIM_Base_Init( &xTim5Handle ))
  {
    if ( HAL_OK == HAL_TIM_Base_Start( &xTim5Handle ))
    {
      pxTimerHandle = &xTim5Handle;
    }
  }
}

/**
  * @brief get timer value
  * @param None
  * @retval timer value
  */
uint32_t hw_timer_get_count( void )
{
  if( pxTimerHandle )
  {
    return __HAL_TIM_GetCounter( pxTimerHandle );
  }
  else
  {
    return 0;
  }
}


